package model;

public class Clienti {
	
	private int id;
	private String nome;
	private String cognome;
	private int eta;
	private int giorniNolo;
	
	public Clienti() {}
	
	

	public int getId() {
		return id;
	}



	public void setId(int id) {
		this.id = id;
	}



	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getCognome() {
		return cognome;
	}

	public void setCognome(String cognome) {
		this.cognome = cognome;
	}

	public int getEta() {
		return eta;
	}

	public void setEta(int eta) {
		this.eta = eta;
	}

	public int getGiorniNolo() {
		return giorniNolo;
	}

	public void setGiorniNolo(int giorniNolo) {
		this.giorniNolo = giorniNolo;
	}
	
	

}
